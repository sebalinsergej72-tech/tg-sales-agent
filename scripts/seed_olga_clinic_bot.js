const now = () => new Date().toISOString();
const id = (prefix) => `${prefix}_${crypto.randomBytes(8).toString("hex")}`;

const result = store.mutate((db) => {
  const bot = db.bots.find((item) => item.username === BOT_USERNAME);
  if (!bot) {
    throw new Error(`Bot @${BOT_USERNAME} not found`);
  }

  let business = db.businesses.find((item) => item.name === businessPatch.name);
  let created = false;
  if (!business) {
    business = {
      id: id("biz"),
      createdAt: now(),
      updatedAt: now(),
      sheetWebhookUrl: "",
      crmWebhookUrl: "",
      timezone: "Europe/Moscow",
      ...businessPatch
    };
    db.businesses.push(business);
    created = true;
  } else {
    Object.assign(business, businessPatch, { updatedAt: now() });
  }

  bot.businessId = business.id;
  bot.updatedAt = now();

  for (const conversation of db.conversations) {
    if (conversation.botId === bot.id) {
      conversation.businessId = business.id;
      conversation.updatedAt = now();
    }
  }

  for (const lead of db.leads) {
    if (lead.botId === bot.id) {
      lead.businessId = business.id;
      lead.updatedAt = now();
    }
  }

  db.events.unshift({
    id: `evt_seed_olga_${Date.now()}`,
    type: "seed.olga-clinic",
    payload: {
      botUsername: BOT_USERNAME,
      businessId: business.id,
      sources: sourceNotes
    },
    createdAt: now()
  });

  return {
    business,
    botUsername: BOT_USERNAME,
    botId: bot.id,
    created,
    sourceNotes
  };
});

